    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String tuKhoa = jTextField1.getText();
            String Key = tuKhoa.substring(0, tuKhoa.indexOf(":"));
            String Truong = tuKhoa.substring(tuKhoa.lastIndexOf(":") + 1);
            String Bang = tuKhoa.substring(tuKhoa.indexOf(":")+ 1, tuKhoa.lastIndexOf(":"));

            switch(Bang) {
                case "SinhVien":
                    switch(kichHoatSV) {
                        case "closing":
                            sv = new SinhVien();
                            sv.setVisible(true);
                            sv.timKiem(Truong, Key, evt);
                            break;
                        case "opening":
                            sv.toFront();
                            break;
                    }
                    break;
                case "MonHoc":
                    switch(kichHoatMH) {
                        case "closing":
                            mh = new MonHoc();
                            mh.setVisible(true);
                            mh.timKiem(Truong, Key, evt);
                            break;
                        case "opening":
                            mh.toFront();
                            break;
                    }
                    break;
                case "LopHoc":
                    switch(kichHoatLH) {
                        case "closing":
                            lh = new LopHoc();
                            lh.setVisible(true);
                            lh.timKiem(Truong, Key, evt);
                            break;
                        case "opening":
                            lh.toFront();
                            break;
                    } 
                    break;
                case "Khoa":
                    switch(kichHoatK) {
                        case "closing":
                            k = new Khoa();
                            k.setVisible(true);
                            k.timKiem(Truong, Key, evt);
                            break;
                        case "opening":
                            k.toFront();
                            break;
                    } 
                    break;
                case "BangDiem":
                    switch(kichHoatBD) {
                        case "closing":
                            bd = new BangDiem();
                            bd.setVisible(true);
                            bd.timKiem(Truong, Key, evt);
                            break;
                        case "opening":
                            bd.toFront();
                            break;
                    }
                    break;
            }
        }
    }//GEN-LAST:event_jTextField1KeyPressed

